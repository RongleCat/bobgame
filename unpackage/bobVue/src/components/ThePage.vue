<template>
  <div class="page-main" :style="{paddingTop:setViewPaddingTop}">
    <div class="statusBar" :style="{height:statusBarHeight,backgroundColor:color?color:'#f2f2f2'}"></div>
    <template v-if="!noHead">
      <div class="pop-header" :style="{top:statusBarHeight,backgroundColor:color?color:'#f2f2f2'}" v-if="!diy">
        <button class="btn-back iconfont" :class="[btnClass]" @click="btnBackEvent"></button>
        <div class="common-header">
          <slot name="headerContent"></slot>
        </div>
      </div>
      <slot v-else name="header"></slot>
    </template>
    <slot name="plug"></slot>
    <transition name="fade-in">
      <div class="skeleton-container" v-if="showSkeleton && !loadDone" :style="{top:setViewPaddingTop}">
        <slot name="gujia" v-if="$slots.gujia"></slot>
        <template v-else>
          <div class="item-img-text h180" v-for="i in 6" :key="i">
            <div class="pic"></div>
            <div class="text">
              <div class="item"></div>
              <div class="item"></div>
              <div class="item"></div>
            </div>
          </div>
        </template>
      </div>
    </transition>
    <transition name="fade-in">
      <div class="view-block" v-if="!showSkeleton || loadDone" :style="{backgroundColor:contentBg?contentBg:'#fff'}" ref="scrollMain" v-scrollfix @scroll="noteTop">
        <slot name="content"></slot>
      </div>
    </transition>
  </div>
</template>

<script>
  export default {
    props: {
      btnType: {
        type: String,
        default: ''
      },
      color: {
        type: String,
        default: '#f2f2f2'
      },
      contentBg: {
        type: String,
        default: '#f0f0f0'
      },
      noHead: {
        type: Boolean,
        default: false
      },
      backUrl: {
        type: String
      },
      loadDone: {
        type: Boolean,
        default: false
      },
      showSkeleton: {
        type: Boolean,
        default: false
      },
      diy: {
        type: Boolean,
        default: false
      }
    },
    data() {
      return {
        headHeight: 88
      }
    },
    computed: {
      btnClass() {
        return this.btnType === 'close' ? 'icon-close' : 'icon-zuo'
      },
      statusBarHeight() {
        return this.$store.state.statusBarHeight + 'rem'
      },
      setViewPaddingTop() {
        if (this.noHead) {
          return '0rem'
        } else {
          return this.$store.state.statusBarHeight + this.headHeight / 75 + 'rem'
        }

      }
    },
    mounted() {
      this.$emit('headerHeight', this.$store.state.statusBarHeight + this.headHeight / 75)
      // console.log(this.color)
      // this.$emit('setPaddingTop', parseInt(this.statusBarHeight) + 88 / 75 + 'rem')
    },
    methods: {
      btnBackEvent() {
        let that = this;
        let backUrl = that.backUrl || this.$route.query.backUrl;
        if (backUrl) {
          let url = backUrl.split('_')[0]
          let type = parseInt(backUrl.split('_')[1])
          if (type) {
            this.$router.replace(url)
          } else {
            this.$router.push(url)
          }
        } else {
          this.$router.go(-1)
        }
      },
      noteTop(e) {
        window.topNote = e.target.scrollTop
        window.viewBlock = e.target
      }
    }
  }
</script>

